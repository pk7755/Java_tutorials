package abstract_class;

public class Maruti extends Car {

	@Override
	public void playMusic() {
		System.out.println("play the music of Maruti car.");
		
	}

	@Override
	public void start() {
		System.out.println("Start the Maruti car");
		
	}

	@Override
	public void stop() {
		System.out.println("Stop the Maruti car");
		
	}

}
