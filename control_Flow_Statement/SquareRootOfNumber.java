package control_Flow_Statement;
import java.util.*;

public class SquareRootOfNumber {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("Enter the number : ");
		int n = sc.nextInt();
		sc.close();
		System.out.println(Math.sqrt(n));
		
	}

}
